// comparison operator compare two value and return a boolean (true and false ) 
// the are useful for marking decision in conditional statements.
console.log(10 > 5); // true
console.log(10 < 5); // false
console.log(10 >= 10); // true
console.log(10 <= 5); // false
console.log(10 == 10); // true
console.log(10 != 5); // true
console.log(10 === "10"); // false
console.log(10 === 10); // true
