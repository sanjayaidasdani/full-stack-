// unary operators operate on a single operand (e.g , increment , decrement ))
let x= 5;
console.log(x); // output: 5
console.log(+x); // output: +5
console.log(-x); // output: -5

console.log(++x); // output: 6 (pre-increment)
console.log(--x); // output: -6 (unary negation of the incremented value)

console.log(!x); // output: false (logical NOT operator, since x is a non-zero value, it is considered true, so !x is false)+ converts a value to a number.
// - negates a value (changes its sign).
// ++ increments a value by 1.
// -- decrements a value by 1.
// ! converts to boolean and reverses the value (true ↔ false).
// typeof returns the data type of a variable.
