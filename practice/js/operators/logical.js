// logical operators are mainly used to perform the logical operations that determine the equality or difference betwween the value 

const a=true;
const b=false;

console.log(a && b); // logical AND operator returns true if both operands are true otherwise it returns false
console.log(a || b); // logical OR operator returns true if at least one of the operands is true otherwise it returns false
console.log(!a); // logical NOT operator returns the opposite of the operand value if a is true it will return false and if a is false it will return true
console.log(!b); // logical NOT operator returns the opposite of the operand value if b is true it will return false and if b is false it will return true