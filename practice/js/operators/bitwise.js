//bitwise operator  perform operations on binary representation of numbers
const res = 5 & 1; // bitwise AND
console.log(res); // 1

const res1 = 5 | 1; // bitwise OR
console.log(res1); // 5

const res2 = 5 ^ 1; // bitwise XOR
console.log(res2); // 4

const res3 = ~5; // bitwise NOT
console.log(res3); // -6

const res4 = 5 << 1;
console.log(res4); // 10

const res5 = 5 >> 1;
console.log(res5); // 2

const res6 = 5 >>> 1;
console.log(res6); // 2